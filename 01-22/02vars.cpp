#include <iostream>

using namespace std;

int main()
{
	cout << "Program to add 2 numbers\n";
	
	int x = 4;
	cout << "First number: " << x << "\n";
	
	int y = 5;
	cout << "Second number: " << y << "\n";
	
	//Sum of two numbers stored in a variable
	int sum = x + y;
	
	//Display result
	cout << x << " + " << y;
	cout << " = " << sum << "\n";
	
	return 0;
}