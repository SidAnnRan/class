#include <iostream>
using namespace std;

int main()
{ 
	cout << "Arithmetic\n";
	int Num1 = 2, Num2 = 3;
    int Num3 = 4, Num4 = 2;
    int Num5 = 4, Num6 = 2;
    int Num7 = 6, Num8 = 3;
    int Num9 = 7, Num10 = 2;
	
	cout << Num1 << " + " << Num2 << " = " << Num1 + Num2 << endl;
	cout << Num3 << " - " << Num4 << " = " << Num3 - Num4 << endl;
	cout << Num5 << " * " << Num6 << " = " << Num5 * Num6 << endl;
	cout << Num7 << " / " << Num8 << " = " << Num7 / Num8 << endl;
	cout << Num9 << " % " << Num10 << " = " << Num9 % Num10 << endl;
	
	return 0;
}