#include <iostream>

using namespace std;

int main()
{
    bool b = true;
    cout << "b is a boolean with value " << b << "\n";

    char c = 'c';
    cout << "c is a character with value " << c << "\n";

    int x = 224;
    cout << "x is an integer with value " << x << "\n";

    float f = 3.2477;
    cout << "f is a float with value " << f << "\n";

    string s = "blah";
    cout << "s is a string with value " << s << "\n";

}